#include <iostream>
using namespace std;
int main() {
    cout<<"5.Specified end value using while loop."<<endl;
    int end;
    cout << "Enter the end value: ";
    cin >> end;
    int i = 1;  
    while (i <= end) {
        cout << i << " ";
        i++;  
    }
    return 0;
}